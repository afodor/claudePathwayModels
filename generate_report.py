def extract_paper_page(pdf_path, page_num, data_dir):
    """Extract a full page from the paper PDF as an image."""
    if not HAS_FITZ:
        return None
    try:
        doc = fitz.open(pdf_path)
        page = doc[page_num]
        mat = fitz.Matrix(3, 3)
        pix = page.get_pixmap(matrix=mat)
        out_path = os.path.join(data_dir, f"_paper_page{page_num}.png")
        pix.save(out_path)
        return out_path
    except Exception as e:
        print(f"  Warning: Could not extract page {page_num}: {e}", flush=True)
        return None


def crop_image(img_path, crop_box, out_path):
    """Crop an image to the given box."""
    from PIL import Image
    img = Image.open(img_path)
    cropped = img.crop(crop_box)
    cropped.save(out_path)
    return out_path


def draw_box(ax, x, y, w, h, text, color='#4a90d9', fontsize=8, text_color='white'):
    """Draw a rounded box with centered text."""
    from matplotlib.patches import FancyBboxPatch
    box = FancyBboxPatch((x - w/2, y - h/2), w, h,
                         boxstyle="round,pad=0.02", facecolor=color,
                         edgecolor='#333333', linewidth=1.2)
    ax.add_patch(box)
    ax.text(x, y, text, ha='center', va='center', fontsize=fontsize,
            fontweight='bold', color=text_color, wrap=True)


def draw_arrow(ax, x1, y1, x2, y2, color='#555555'):
    """Draw an arrow between two points."""
    ax.annotate('', xy=(x2, y2), xytext=(x1, y1),
                arrowprops=dict(arrowstyle='->', color=color, lw=1.5))



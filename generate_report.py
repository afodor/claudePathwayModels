def parse_importance_csv(fpath):
    """Parse pathway importance CSV, handling commas in pathway names."""
    pathways, perm_imp, sens_abs, sens_signed = [], [], [], []
    with open(fpath) as f:
        next(f)  # skip header
        for line in f:
            line = line.strip()
            # Split from right — last 3 fields are always floats
            parts = line.rsplit(',', 3)
            pathways.append(parts[0])
            perm_imp.append(float(parts[1]))
            sens_abs.append(float(parts[2]))
            sens_signed.append(float(parts[3]))
    return pathways, np.array(perm_imp), np.array(sens_abs), np.array(sens_signed)

